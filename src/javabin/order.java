package javabin;

public class order {
	private String gamedlcname;
	private String saledate;
	private float paymoney;
	
	public order() {
		this.gamedlcname = gamedlcname;
		this.saledate = saledate;
		this.paymoney = paymoney;
	}

	public String getGamedlcname() {
		return gamedlcname;
	}

	public void setGamedlcname(String gamedlcname) {
		this.gamedlcname = gamedlcname;
	}

	public String getSaledate() {
		return saledate;
	}

	public void setSaledate(String saledate) {
		this.saledate = saledate;
	}

	public float getPaymoney() {
		return paymoney;
	}

	public void setPaymoney(float paymoney) {
		this.paymoney = paymoney;
	}
	
	
}
