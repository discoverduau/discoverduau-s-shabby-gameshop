package javabin;

public class gameshop {
	private String gamename;
	private String systemneed;
	private String evaluation;
	private int price;
	private int agelim;
	private String producer;
	private float presentage;
	private String classes;
	private String introduction;
	private String downloadweb;
	private String ondate;
	public gameshop() {
		this.gamename = gamename;
		this.systemneed = systemneed;
		this.evaluation = evaluation;
		this.price = price;
		this.agelim = agelim;
		this.producer = producer;
		this.presentage = presentage;
		this.classes = classes;
		this.introduction = introduction;
		this.downloadweb = downloadweb;
		this.ondate = ondate;
	}
	public String getGamename() {
		return gamename;
	}
	public void setGamename(String gamename) {
		this.gamename = gamename;
	}
	public String getSystemneed() {
		return systemneed;
	}
	public void setSystemneed(String systemneed) {
		this.systemneed = systemneed;
	}
	public String getEvaluation() {
		return evaluation;
	}
	public void setEvaluation(String evaluation) {
		this.evaluation = evaluation;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getAgelim() {
		return agelim;
	}
	public void setAgelim(int agelim) {
		this.agelim = agelim;
	}
	public String getProducer() {
		return producer;
	}
	public void setProducer(String producer) {
		this.producer = producer;
	}
	public float getPresentage() {
		return presentage;
	}
	public void setPresentage(float presentage) {
		this.presentage = presentage;
	}
	public String getClasses() {
		return classes;
	}
	public void setClasses(String classes) {
		this.classes = classes;
	}
	public String getIntroduction() {
		return introduction;
	}
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	public String getDownloadweb() {
		return downloadweb;
	}
	public void setDownloadweb(String downloadweb) {
		this.downloadweb = downloadweb;
	}
	public String getOndate() {
		return ondate;
	}
	public void setOndate(String ondate) {
		this.ondate = ondate;
	}
	
	
	

}
